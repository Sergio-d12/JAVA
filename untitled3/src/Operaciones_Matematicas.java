public class Operaciones_Matematicas {
    public static void main(String[] args) {
        double x = 2.5;
        double y = 3;
        System.out.println(Math.ceil(x));// entero hacia arriba (Redondeo)
        System.out.println(Math.floor(x));//entero hacia abajo (redondeo a la inversa)
        System.out.println(Math.pow(x,y));//elevar numero al cuadrado
        System.out.println(Math.max(x,y));//numero maxima
        System.out.println(Math.min(x,y));//numero minimo
        System.out.println(Math.sqrt(y));//raiz cuadadra
        System.out.println(Math.PI * Math.pow(x,3));//pi por R a la 2 area del circulo


    }
}
